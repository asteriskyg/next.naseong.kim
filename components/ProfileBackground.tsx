export const ProfileBackground = ({ user }: { user?: UserType }) => {
  const [scrollDepth, setScrollDepth] = useState(0);

  const updateScrollDepth = () => {
    setScrollDepth(window.scrollY);
  };

  useEffect(() => {
    const watchScroll = () => {
      window.addEventListener("scroll", updateScrollDepth);
    };
    watchScroll();

    return () => {
      window.removeEventListener("scroll", updateScrollDepth);
    };
  }, []);

  const translateY = {
    transform: `translateY(${scrollDepth * 0.5}px)`,
  };

  if (!user?.profileBackgroundUrl)
    return (
      <div
        className="w-full aspect-w-2 aspect-h-1 sm:aspect-w-5 sm:aspect-h-2 md:aspect-w-7 md:aspect-h-2 lg:aspect-w-9 lg:aspect-h-2 bg-slate-100"
        style={translateY}>
        <div style={BackgrounPattern} />
      </div>
    );

  return (
    <div
      className="w-full aspect-w-2 aspect-h-1 sm:aspect-w-5 sm:aspect-h-2 md:aspect-w-7 md:aspect-h-2 lg:aspect-w-9 lg:aspect-h-2 bg-slate-100"
      style={translateY}>
      <Image src={`https://cdn.naseong.kim/${user.profileBackgroundUrl}`} alt="" fill className="object-cover" />
    </div>
  );
};
